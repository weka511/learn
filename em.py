from argparse import ArgumentParser
from os.path import basename,join
from pathlib import Path
from time import time
from warnings import filterwarnings
from scipy.stats import norm
from matplotlib.pyplot import figure, rcParams, show
import numpy as np
from gmm import GaussionMixtureModel
from utils import generate_xkcd_colours

def maximize_likelihood(xs, mu=np.array(0), sigma=np.ones((1)), alpha=np.ones((1)), K=3, N=250,
                        rtol=1.0e-6, n_burn_in=3):
    '''
    Determine the clusters that maximize likelihood

    Parameters:
        xs         Data series
        mu         Starting values for means
        sigma      Starting values for standard deviations
        alpha      Starting value for proportion of data points assigned to each cluster
        K          Number of Gaussians that we are using to model data
        N          Maximum number of iterations
        rtol       Tolerance: used to decide whether algorithm has converged
        n_burn_in  Number of steps before we start checking for convergence
        
    Returns:
        List of likelihoods, one for each step
        Centres for all clusters
        Standard deviations
        Proportion of data points assigned to each cluster
    '''
    def get_log_likelihood(mu=np.array(0), sigma=np.ones((1)), alpha=np.ones((1))):
        '''
        Calculate log of likelihood that xs were generated by current set of parameters

        Parameters:
            mu         Means
            sigma      Standard deviations
            alpha      Proportion of data points assigned to each cluster
            
        Returns:
            Log of likelihood
        '''
        def get_one_log_likelihood(x,loc,scale,epsilon=1e-6):
            return - 0.5*((x-loc)/max(scale,epsilon))**2 #FIXME
 
        return sum(get_one_log_likelihood(xs[i], mu[k], sigma[k]) for i in range(len(xs)) for k in range(K))

    def e_step(mu=np.array(0), sigma=np.ones((1)), alpha=np.ones((1))):
        '''
        Perform E step from EM algorithm, which assigns points to clusters
        
        Parameters:
            mu         Means
            sigma      Standard deviations
            alpha      Proportion of data points assigned to each cluster
        
        Returns:
            w[i,k] the membership of ith point in cluster k
        '''
        w = np.empty((len(xs),K))
        for i in range(len(xs)):
            for k in range(K):
                w[i,k] = norm.pdf(xs[i],loc=mu[k],scale=max(sigma[k],0.002)) * alpha[k]  #FIXME

        row_sums = w.sum(axis=1)
        return w / row_sums[:, np.newaxis]

    def m_step(w):
        '''
        Perform M step from EM algorithm
        
        Parameters:
             w      w[i,k] is the membership of ith point in cluster k
        
        Returns:
            List of likelihoods, one for each step
            Centres for all clusters
            Standard deviations
            Proportion of data points assigned to each cluster
        '''
        alpha = np.sum(w,axis=0)/np.sum(w)   # Proportion of data points assigned to each k

        mu = np.empty((K))
        for k in range(K):
            mu[k] = np.dot(w[:,k],xs)
        mu /= np.sum(w,axis=0)

        sigma = np.zeros((K))
        for k in range(K):
            sigma[k] += np.dot(w[:,k], (xs[:] - mu[k])**2)
        sigma = sigma/ np.sum(w,axis=0)

        return (alpha, mu, sigma)

    # Use E-step and M- Step to update alpha, mu, and sigma as long as likelihoods keep improving

    likelihoods = []

    for i in range(N):
        likelihoods.append(get_log_likelihood(mu=mu,sigma=sigma,alpha=alpha))
        if i > n_burn_in and abs(likelihoods[-1] / likelihoods[-2] - 1) < rtol:
            return np.array(likelihoods), alpha, mu, sigma

        alpha, mu, sigma = m_step(e_step(mu=mu,sigma=sigma,alpha=alpha))
    return np.array(likelihoods), alpha, mu, sigma
        

def plot_data(xs, mu0, mu, sigma, ax=None):
    '''
    Plot the data and the fitted Gaussian
    '''
    def normalize(ys,n):
        '''
        Used to scale a probability density so it fits into the same y range as an empirical distribution

        Parameters:
            ys     Points specifiying probability density
            n      Empirical distribution
        '''
        return (n.max()/ ys.max()) * ys
    
    colour_generator = generate_xkcd_colours()
    n, bins, _ = ax.hist(xs, bins=50, 
                         label=fr'Data',
                         color=next(colour_generator))
    midpoints = (bins[:-1] + bins[1:]) / 2
    
    for k in range(len(mu)):
        density = normalize(norm.pdf(midpoints, loc=mu[k], scale=sigma[k]),n)
        ax.plot(midpoints,density,
                color=next(colour_generator),
                label=fr'EM $\mu=${mu[k]:.3f}, $\sigma=${sigma[k]:.3f}')    
        
    y0,y1 = ax.get_ylim()
    ax.vlines(mu,y0,y1,color=next(colour_generator),label=r'$\mu$'f'{mu}')
    ax.vlines(mu0,y0,y1,color=next(colour_generator),linestyles='dashed',label=r'$\mu_0$'f'{mu0}')
    ax.set_title('Data compared to  EM')
    ax.set_ylabel('p')
    ax.set_xlabel('x')
    ax.legend()

def plot_likelihoods(Likelihoods, ax=None):
    '''
    Plot the likelihood computed at each step
    '''
    ax.set_title('Progress')
    ax.plot(Likelihoods,label='Log Likelihood',c='xkcd:magenta')
    ax.set_xticks(range(1, len(Likelihoods)))
    ax.set_ylabel('Log Likelihood')
    ax.set_xlabel('Iteration')
    ax.legend()

def parse_args():
    parser = ArgumentParser(__doc__)
    parser.add_argument('--N', type=int, default=5000, help='Dataset size')
    parser.add_argument('--mean', type=float, default=0.5, help='Mean for dataset')
    parser.add_argument('--sigma', type=float, default=0.5, help='Standard deviation')
    parser.add_argument('--seed', type=int, default=None, help='Seed for random number generator')
    parser.add_argument('--show', action='store_true', default=False, help='Show plots')
    parser.add_argument('--figs', default='./figs', help='Folder to store plots')
    parser.add_argument('--load', default = None, help='Data to be loaded from a file generated by gmm.py')
    parser.add_argument('--data', default='./data', help='Path to folder where data are stored')
    return parser.parse_args()

def estimate_initial_means(xs,K,m=1000,rng = np.random.default_rng()):
    '''
    Choose starting values for means
    
    Parameters:
        xs
        K
        m
        rng 
    '''
    n = len(xs)
    if n > m:
        n = m
        xs0 = rng.choice(xs,size=n)
    else:
        xs0 = xs
    xs1 = np.sort(xs0)
    indices = [(n*i)//(K+1) for i in range(1,K+1)]
    return np.sort(xs1[indices])

def get_starting_values(load,mean,sigma, N,data,rng = np.random.default_rng()):
    '''
    Generate or load data, and assign initial values to parameters
    
    Parameters:
        load
        mean
        sigma
        N
        data
    '''
    if load == None:
        xs = rng.normal(loc=mean, scale=sigma, size=N)
        K = 1
        mu = rng.choice(xs,size=K)
    else:
        path_name = Path(data) / load
        model = GaussionMixtureModel()
        xs = np.ravel(model.load(path_name.with_suffix('.npz')))
        K = model.mu.shape[0]
        mu = estimate_initial_means(xs,K,rng=rng)
   
    sigma = np.ones((K))
    alpha = np.ones((K))/K
    return xs,K,mu,sigma,alpha
    
if __name__ == '__main__':
    rcParams.update({
        'text.usetex' : True
    })
    filterwarnings('error')
    args = parse_args()
    rng = np.random.default_rng(args.seed)
    start = time()

    xs,K,mu0,sigma0,alpha0 = get_starting_values(args.load,args.mean,args.sigma, args.N,args.data,rng=rng)   
    L, _, mu, sigma = maximize_likelihood(xs,mu=mu0,sigma=sigma0,alpha=alpha0,K=K)
    fig = figure(figsize=(10, 10))
    plot_data(xs, mu0, mu, sigma, ax=fig.add_subplot(2,1,1))

    plot_likelihoods(L, ax=fig.add_subplot(2,1,2))
    fig.tight_layout(h_pad=2,pad=5)
    fig.suptitle('Gaussian Model fitted by Expectation Maximization')
    fig.savefig(join(args.figs,basename(__file__).split('.')[0]))

    print(f'N={args.N},  EM: {(time() - start):.3f} sec')
    if args.show:
        show()
